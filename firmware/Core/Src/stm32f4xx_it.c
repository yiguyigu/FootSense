/* USER CODE BEGIN 1 */
void HAL_UART_RxCpltCallback(UART_HandleTypeDef *huart)
{
    if (huart->Instance == USART1 )                    /* 如果是串口1 */
    {
        if ((g_usart1_rx_sta & 0x8000) == 0)             /* 接收未完成 */
        {
            if (g_usart1_rx_sta & 0x4000)                /* 接收到了0x0d（即回车键） */
            {
                if (Buffer1 [0] != 0x0a)             /* 接收到的不是0x0a（即不是换行键） */
                {
                    g_usart1_rx_sta = 0;                 /* 接收错误,重新开始 */
                }
                else                                    /* 接收到的是0x0a（即换行键） */
                {
                    g_usart1_rx_sta |= 0x8000;           /* 接收完成了 */
                }
            }
            else                                        /* 还没收到0X0d（即回车键） */
            {
                if (Buffer1 [0] == 0x0d)
                    g_usart1_rx_sta |= 0x4000;
                else
                {
                   g_usart1_rx_buffer [g_usart1_rx_sta & 0X3FFF] = Buffer1[0];
                    g_usart1_rx_sta++;
                    if (g_usart1_rx_sta > (200 - 1))
                    {
                        g_usart1_rx_sta = 0;             /* 接收数据错误,重新开始接收 */
                    }
                }
            }
        }

    }
}
